-- Creación de tablas
CREATE TABLE ALMACEN (
    ID INT PRIMARY KEY,
    CAPACIDAD_MAX INT,
    OCUPACION INT,
	NOMBRE INT
);

CREATE TABLE PRODUCTO (
    ID INT PRIMARY KEY,
    PRECIO DOUBLE,
    MODELO VARCHAR(20),
    NUM_UNIDADES INT,
    MARCA VARCHAR(20),
    ID_ALMACEN INT,
    ID_PROVEEDOR INT,
    FOREIGN KEY (ID_ALMACEN) REFERENCES ALMACEN(ID),
    FOREIGN KEY (ID_PROVEEDOR) REFERENCES PROVEEDOR(ID)
);

CREATE TABLE PROVEEDOR (
    ID INT PRIMARY KEY,
	Nombre VARCHAR(20)
);

CREATE TABLE EMPLEADO (
    ID INT PRIMARY KEY,
    TELEFONO INT,
    HORARIO VARCHAR(20),
    DNI VARCHAR(9)
);

CREATE TABLE COMPLETO (
    ID INT PRIMARY KEY,
    HORAS_EXTRA DOUBLE,
    FOREIGN KEY (ID) REFERENCES EMPLEADO(ID)
);

CREATE TABLE PARCIAL (
    ID INT PRIMARY KEY,
    HORAS_SEMANALES DOUBLE,
    FOREIGN KEY (ID) REFERENCES EMPLEADO(ID)
);

CREATE TABLE CLIENTE (
    ID INT PRIMARY KEY,
    DNI VARCHAR(9),
    NOMBRE VARCHAR(20)
);

CREATE TABLE SOCIO (
    ID INT PRIMARY KEY,
    PUNTOS INT,
    NUM_SOCIO INT,
    FOREIGN KEY (ID) REFERENCES CLIENTE(ID)
);

CREATE TABLE NOSOCIO (
    ID INT PRIMARY KEY,
    NUM_VISITAS VARCHAR(50),
    FOREIGN KEY (ID) REFERENCES CLIENTE(ID)
);

CREATE TABLE VENTA (
    ID INT PRIMARY KEY,
    METODO_PAGO VARCHAR(20),
    PRECIO DOUBLE,
    DESCUENTO DOUBLE,
    ID_EMPLEADO INT,
    ID_CLIENTE INT,
    FOREIGN KEY (ID_EMPLEADO) REFERENCES EMPLEADO(ID),
    FOREIGN KEY (ID_CLIENTE) REFERENCES CLIENTE(ID)
);

